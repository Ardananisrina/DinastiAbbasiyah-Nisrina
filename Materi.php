			<h1>Ilmuwan Yang Ada Pada Masa Dinasti Abbasiyah</h1>
			<p><b>B. Ilmuwan Pada Masa Dinasti Abbasiyah</b></p>
      <p>Dinasti Abbasiyah berkuasa lebih dari lima abad, sejak 132-656 H/750
        sampai 1258 M. Ia merupakan dinasti Islam yang memberikan sumbangan
        besar bagi kegemilangan peradaban Islam. Dengan dukungan para khalifah
        yang memiliki perhatian besar bagi pengembangan ilmu pengetahuan dan
        peradaban, maka lahirlah banyak ilmuan dan ulama yang memiliki pemikiran
        cemerlang. Karya-karya mereka abadi sepanjang sejarah. Hal tersebut
        membuktikan bahwa peradaban dan kebudayaan Islam memberi sumbangan besar
        bagi peradaban dunia. Untuk mengenal lebih dekat ilmuwan dan ulama besar
        tersebut, berikut uraiannya.
      </p>
			<p><b>1. Ali Ibnu Rabbani at-Tabari (838-870M), Penemu Ensiklopedia Kedokteran</b></p>
			<p>Abu Al-Hasan Ali bin Sahl Rabban aṭ-Ṭabari berasal dari keluarga Yahudi
				Syiria terkenal di Merv. Ia pindah ke Tabaristan sehingga dikenal dengan
				nama Aṭ-Ṭabari. Ayahnya  Sahl bin Bisyr adalah seorang pejabat negara,
				berpendidikan tinggi, dan dihormati masyarakat. Ali bin Sahl aṭ-Ṭabari
				masuk Islam pada masa kekhalifahan Al-Mu’taṣim. Ia mahir berbahasa Syiria
				dan Yunani, dua bahasa yang menjadi sumber dalam tradisi pengobatan
				kuno. Selanjutnya, Aṭ-Ṭabari dikenal sebagai seorang dokter. Dia juga
				menjadi ilmuwan yang menulis ensiklopedia kedokteran.
			</p>
			<p>Karyanya yang terkenal berjudul Firdaus al-Hikmah, ditulis setelah
				ia memeluk agama Islam. Buku tersebut ditulis dalam bahasa Arab,
				kemudian diterjemahkan sendiri ke dalam bahasa Syiria. Secara garis
				besar, buku ini dibagi ke dalam tujuh bagian/bab. Bab pertama berjudul
				Kulliyat at-Ṭibb, memuat masalah doktrin ilmu kesehatan terkini. Bab
				kedua menguraikan bagian-bagian organ tubuh manusia, peraturan tentang
				mejaga kesehatan, dan laporan tentang penyakit-penyakit yang menghinggapi
				otot. Bab ketiga menguraikan tentang diet. Bab keempat berisi tentang
				seluruh penyakit yang biasa menimpa badan. Bab kelima menguraikan tentang
				rasa dan warna. Bab keenam menjelaskan tentang obat-obatan dan racun.
				Sedangkan bab ketujuh membicarakan tentang astronomi, juga ringkasan
				pengobatan ala India.
			</p>
			<p>Ali Rabbani aṭ-Ṭabari bukan hanya seorang dokter, tetapi juga
				ilmuwan yang menguasai berbagai ilmu, diantaranya ahli dalam
				ilmu astronomi, ﬁ lsafat, matematika, dan sastra. Ia merupakan
				guru Zakaria Abu Bakar ar-Razi, seorang ahli pengobatan Muslim
				terkenal lainnya.
			</p>
			<p><b>2. Abu Ali al-Husain bin Abdullah bin Sina / Ibnu Sina (980-1037M)</b></p>
			<p>Di dunia Barat, Ibnu Sina dikenal dengan nama Avvicenna. Ia lahir
				bulan Ṣafar 370 H/ Agustus 980 M di Ifsyina, suatu negeri kecil
				dekat Charmitan, Bukhara. Orang tuanya pejabat tinggi pada masa
				Dinasti Saman. Ibnu Sina dibesarkan di Bukhara. Pada usia sepuluh
				tahun, ia sudah banyak mempelajari ilmu agama Islam dan berhasil
				menghafal Al-Quran. Dari Abu Abdellah Natili, Ibnu Sina belajar
				ilmu logika melalui buku Isagoge dan Porphyry, Euclid dan Al-Magest
				Ptolemus. Setelah itu, ia mendalami metaﬁ sika Plato dan Aristoteles.
			</p>
			<p>Dalam dunia kedokteran, Ibnu Sina adalah ilmuwan Muslim pertama
				yang menemukan peredaran darah manusia. Teorinya  disempurnakan
				oleh William Harvey enam ratus tahun kemudian. Dia juga yang
				pertama kali mengatakan bahwa selama dalam kandungan, bayi
				mengambil makanannya lewat tali pusar. Dia pula yang mengawali
				praktek bedah dan penjahitannya. Selain itu, ia terkenal
				sebagai dokter ahli jiwa yang kini disebut psikoterapi.
			</p>
			<p> Karyanya berupa buku mencapai 200 judul, yang meliputi
				bidang ﬁ lsafat, kedokteran, geometri, astronomi, teologi,
				ﬁ lologi, dan kesenian. Karya terbesarnya berjudul
				Al-Qanūn fī aṭ-Ṭibb. Buku ini merupakan kumpulan pemikiran
				Yunani-Arab tentang kedokteran. Karya Ibnu Sina tersebut
				menjadi rujukan bagi para mahasiswa kedokteran dari abad
				ke-12 sampai abad  ke-17 M. Isinya antara lain menjelaskan
				pembedaan mediastinum dengan pleurisy (pembengkakan pada
				paru-paru); mengenai kemungkinan penalaran penyakit phthisis
				(penyakit saluran pernafasan, terutama asma dan TBC) melalui
				pernafasan; dan penyebaran berbagai penyakit melalui air dan
				debu. Ibnu Sina juga memberikan diagnosa ilmiah tentang
				penyakit ancylostomisis (infeksi usus kecil akibat cacing)
				dan menyebutkan cacing pita sebagai penyebabnya. Secara
				keseluruhan, terdapat 170 jenis obat-obatan disebutkan
				dalam buku tersebut.
			</p>
			<p><b>3. Abu Bakar Muhammad bin Zakariya ar-Razi (864-930M)</b></p>
			<p>Abu Bakar Muhammad bin Zakaria ar-Razi, berasal dari
				Persia, lahir di Rayy pada tahun 865 M. Di dunia Barat,
				ia dikenal dengan panggilan ‘Ar-Razes. Ar-Razi adalah
				murid Ali bin Sahl Rabban aṭ-Ṭabari yang cemerlang.
				Setelah mempelajari matematika, astronomi, logika,
				sastra, dan kimia, ia memusatkan perhatiannya pada
				kedokteran dan ﬁ lsafat. Ia menjadi seorang dokter
				dan ﬁ losof  besar pada zamannya.
			</p>
			<p> Karya Ar-Razi mencapai 232 buku atau risalah dan
				kebanyakan dalam bidang kedokteran. Karya tulis hasil
				penelitiannya yang termashur adalah Al-Ḥawī, sebuah
				ensiklopedi kedokteran yang terdiri dari 20 jilid.
				Buku ini berisi ilmu kedokteran Yunani, Arab, dan
				diterjemahkan ke dalam bahasa Latin pada tahun 1279 M.
				Sejak saat itu, buku tersebut menjadi rujukan berbagai
				universitas di Eropa sampai abad ke-17 M. Buku terkenal
				lainnya berjudul Fī al-Judarī wa al-Ḥaṣbat, membahas
				tentang penyakit campak dan cacar, juga diterjemahkan
				ke bahasa Latin. Pada tahun 1866 M, buku itu dicetak
				ulang yang ke-40 kalinya. Ar-Razi  wafat pada tahun 932 M
				di kota kelahirannya.
			</p>
			<p><b>4. Abu Yusuf Ya'qub bin Ishaq al-Sabbah al-Kindi (801-873M), Filosof Muslim Pertama</b></p>
			<p>Nama lengkapnya Abu Yusuf Ya’qub bin Ishak bin Sabah bin
				Imran bin Ismail bin Muhammad bin al-Asy’aṡ bin Qais al-Kindi.
				Nama Al-Kindi berasal dari nama salah satu suku Arab terbesar
				sebelum Islam, yaitu suku Kindah. Al-Kindi lahir di Kufah
				pada tahun 185 H /801 M, pada masa kekhalifahan Harun ar-Rasyid.
				Ayahnya bernama Ibnu as-Sabah, pernah menjadi Gubernur Kufah
				pada masa kekhalifahan Al-Mahdi (775 M – 785 M) dan Harun ar-Rasyid
				(786 M – 809 M). Kakeknya, Al-Asy’aṡ bin Qais, dikenal sebagah
				salah seorang sahabat Nabi Muhammad  Saw.
			</p>
			<p>Al-Kindi sosok yang dikenal berotak “encer”. Tiga bahasa penting,
				yaitu Yunani, Suryani, dan Arab dikuasainya. Suatu kelebihan yang
				jarang dimiliki orang pada saat itu. Al-Kindi adalah ﬁlosof Muslim
				pertama. Ia orang Islam pertama yang mendalami ilmu-ilmu ﬁlsafat.
				Pada saat itu, sampai abad ke-7 M, pengetahuan ﬁlsafat masih
				didominasi orang-orang Kristen Suriah. Al-Kindi menerjemahkan dan
				menyimpulkan karya-karya ﬁlsafat Helenisme (banyak dipengaruhi
				pemikiran Yunani kuno). Ia juga dikenal sebagai pemikir Muslim
				pertama yang menyelaraskan ﬁlsafat dan agama. Al-Kindi memandang
				ﬁlsafat sebagai ilmu yang mulia. Dia melukiskan ﬁlsafat sebagai
				ilmu dari segala ilmu dan kearifan dari segala kearifan. Filsafat
				bertujuan untuk memperkuat kedudukan agama, sekaligus menjadi
				bagian dari kebudayaan Islam.
			</p>
			<p>Al-Kindi menguasai beragam ilmu pengetahuan. Sebanyak 270 karya telah
				berhasil ditulisnya. Karya tersebut dapat dikelompokkan dalam bidang
				ﬁlsafat, logika, ilmu hitung, musik, astronomi, geometri, medis,
				astrologi, psikologi, politik, dan meteorologi. Salah satu karya Al-Kindi
				di bidang ﬁlsafat adalah Risālah fī Madkhal al-Mantīq bi Istifā al-Qawl fīh,
				berisi tentang pengatar ilmu logika. Dia menjadi salah satu ilmuwan besar,
				sekaligus bukti hidup kegemilangan kebudyaaan Islam pada era kejayaan
				Dinasti Abbasiyah. Ia bahkan pernah diangkat sebagai guru dan tabib kerajaan.
				Al-Kindi meninggal pada tahun 869 M.
			</p>
			<p><b>5. Abu Hamid Muhammad bin Muhammad al-Gazali al-Tusi al-Syafi'i (1058-1111M)</b></p>
			<p>Nama lengkap Imam al-Gazali ialah Muhammad bin Ahmad al-Imam al-Jalil Abu
				Hamid aṭ-Ṭusi al-Gazali, lahir di Ṭusi (daerah Khurasan di wilayah Persia
				[Iran sekarang]) pada tahun 450 H /1058 M. Ayahnya seorang pemintal benang
				dan ahli tasawuf yang hebat.
			</p>
			<p> Al-Gazali belajar ilmu ﬁ qh kepada Syekh Ahmad bin Muhammad ar-Razikani,
				teman ayahnya sekaligus orang tua asuhnya. Selain itu, ia belajar kepada
				Imam Abi Nasar al-Isma’ili di negeri Jurjan. Al-Gazali kemudian berangkat
				ke Nisafur dan belajar kepada Imam alHaramain al-Juwaini, guru besar di
				Madrasah Niẓamiyah Nisafur. Dengan cepat Al-Gazali dapat menguasai ilmu–
				ilmu pengetahuan pokok, seperti ilmu mantiq (logika), ﬁlsafat, dan ﬁkih
				mażhab Syaﬁ ’i. Karena kecerdasannya, Imam al-Haramain mengatakan bahwa
				Al-Gazali itu adalah “lautan tak bertepi".
			</p>
			<p>Al-Gazali selalu hidup berpindah-pindah, khususnya untuk mendalami ilmu
				pengetahuan. Setelah dari Baghdad, dia berangkat ke Syam, menetap hampir
				2 (dua) tahun untuk berlatih membersihkan diri, menyucikan hati dengan
				mengingat Tuhan, juga i’tikaf di mesjid Damaskus. Berikutnya pindah ke
				Palestina untuk mengunjungi kota Hebron dan Jerussalem. Kedua kota itu
				merupakan tempat para Nabi mendapat wahyu pertama dari Allah, semenjak
				dari Nabi Ibrahim sampai Nabi Isa. Al-Gazali juga berangkat ke Mesir,
				pusat kemajuan dan kebesaran Islam kedua sesudah Baghdad. Dari kota
				Kairo, ia melanjutkan perjalanan ke Iskandariyah di Mesir. Setelah itu,
				ia berangkat ke Mekkah untuk menunaikan ibadah haji, juga berzirah ke
				kuburan Nabi Ibrahim. Akhirnya, Al-Gazali kembali ke Naisabur dan mendirikan
				Madrasah Fiqh, sekaligus asrama (khanaqah) untuk melatih para mahasiswa
				dalam paham dan amalan suﬁ.
			</p>
			<p>Al-Gazali menulis banyak kitab di berbagai bidang ilmu pokok pada zamannya,
				antara lain tentang tafsir Al-Quran, ilmu kalam, uṣul ﬁkih, ﬁkih, tasawuf,
				mantiq, ﬁlsafat, dan lainlain. Karyanya yang paling terkenal adalah Iḥyā ‘Ulūm ad-Dīn,
				yang membahas ilmu-ilmu agama. Al-Ghazali wafat dalam usianya yang ke 55 pada
				tahun 505 H (1111 M) di Tusi (di wilayah Iran sekarang), tepat di kota tempat
				kelahirannya.
			</p>
			<p><b>6. Ahmad bin Muhammad bin Ya'qub bin Miskawaih (932-1030M)</b></p>
			<p>Nama lengkapnya Ahmad bin Muhammad bin Ya’qub bin Miskawaih, tetapi
				lebih dikenal Ibnu Miskawaih atau Maskawaih. Nama itu diambil dari nama
				kakeknya yang semula beragama Majusi (Persia), kemudian masuk Islam.
				Julukannya adalah Abu ’Ali, yang merujuk kepada sahabat ’Ali bin Abi
				Ṭalib. Di samping itu, ia juga bergelar al-Khazin yang berarti bendaharawan.
				Jabatan sebagai bendaharawan/menteri keuangan itu berlangsung pada masa
				kekuasaan ’Aḍud ad-Daulah dari Bani Buwaih (al-Dawlah al-Buwaihiyyah).
			</p>
			<p>Tidak banyak informasi yang menjelaskan riwayat pendidikannya. Menurut
				sejarawan Ahmad Amin, pendidikan anak-anak pada zaman Abbasiyah pada
				umumnya dimulai dengan belajar membaca, menulis, mempelajari Al-Quran
				dan dasar-dasar bahasa Arab (Nahwu) serta membuat syair. Dilanjutkan
				dengan mempelajari ilmu ﬁ kih, sejarah, matematika, dan ilmu-ilmu praktis
				seperti ilmu musik, catur, dan militer. Ibnu Miskawaih sendiri belajar
				sejarah dari Abu Bakar Ahmad bin Kamil al-Qadi, belajar ﬁlsafat dari Ibnu
				al-Akhmar, dan belajar kimia dari Abu Ṭayyib. Ia juga berkawan dengan para
				ilmuwan lain, diantaranya Ibnu Sina.
			</p>
			<p>Ibnu Miskawaih dikenal sebagai sejarawan besar, kemasyhurannya melebihi
				pendahulunya, yaitu Aṭ-Ṭabari. Ia adalah seorang dokter, penyair, ahli
				bahasa, dan ﬁlosof Muslim yang mampu memadukan metode pemikiran Yunani
				dan Islam. Di samping itu, ia juga ahli dalam ﬁlsafat Romawi, India, Arab,
				dan Persia. Miskawaih memiliki perhatian besar terutama pada ﬁlsafat etika Islam.
				Hal ini terlihat dari berbagai buku atau karyanya, diantaranya:
				1) Risalah ﬁ  al-Lazzat wa al-Alam;
				2) Risālah fī aṭ-Ṭabi’at;
				3) Risālah fī Jauhar an-Nafs;
				4) Maqālat an-Nafs wa al-’Aql;
				5) Fī Iṡbāt aṣ-Ṣuwār al-Ruḥāniyat allatī lā Yabula Lama, min Kitāb al-’Aql wa al-Ma’qūl;
				6) Ta’rīf li Miskawaih Yumayyizu bihi bain ad-Dahr wa azZamān;
				7) Taḥzīb al-Akhlāq wa Taṭṭhir al-A’raq dan;
				8) Risālah fī Jawāb fī Su’al li ‘Ali ibn Miskawaih Ilā Abī Ḥayyān aṣ-Ṣauli fī Haqīqat al-’Adl.
				Oleh sebab itu, Ibnu Miskawaih menjadi ilmuwan Muslim pertama di bidang ﬁ lsafat akhlak.
			</p>
			<p><b>7. Abu Musa Jabir bin Hayyan (750-803M)</b></p>
			<p>Orang Barat mengenalnya dengan  sebutan ‘Geber’. Abu Musa
				Jabir bin Hayyan lahir di Kufah pada tahun 750 M. Sumbangan
				terbesar Jabir bin Hayyan dalam dunia ilmu pengetahuan adalah
				di bidang kimia. Keahlian itu didapatnya dari seorang guru
				bernama Barmaki Vizier, tepatnya pada era pemerintahan Harun
				ar-Rasyid di Baghdad. Ia mengembangkan teknik percobaan secara
				terencana dan beraturan di dalam penelitian kimia, sehingga
				setiap eksperimen dapat direproduksi kembali.
			</p>
			<p>Jabir menekankan bahwa jumlah zat itu berhubungan dengan
				reaksi kimia yang terjadi. Penemuan Jabir itu dapat dipandang
				telah merintis ditemukannya hukum perbandingan tetap.
				Sumbangan lainnya yang penting antara lain dalam penyempurnaan
				proses kristalisasi (pengabluran atau penjernihan), distilasi
				(pemanasan benda cair atau padat menjadi uap), kalsinasi
				(pemanasan suatu benda hingga temperaturnya tinggi), sublimasi
				(proses perubahan langsung zat padat menjadi cair), dan penguapan
				serta pengembangan peralatan untuk melakukan proses-proses
				tersebut.
			</p>
			<p>Jabir juga menulis kitab-kitab penting bagi pengembangan ilmu kimia,
				antara lain: Kitāb al-Kimyā, Kitāb al-Sab’īn, Kitāb ar-Raḥmah, At-Tajmī,
				Al-Zilāq al-Ṣarqī, Book of the Kingdom, Book of Eastern Mercury,
				dan Book of Balance.
			</p>
			<p><b>8. Muhammad bin Musa al-Khawarizmi (780-850M)</b></p>
			<p>Nama lengkap Al-Khawarizmi adalah Muhammad bin Musa alKhawarizmi
				atau Abu Abdullah Muhammad bin Ahmad bin Yusuf.  Di dunia Barat,
				ia dikenal sebagai Al-Khawarizmi, Al-Cowarizmi, AlAhawizmi, Al-Karismi,
				Al-Goritmi, Al-Gorismi, dan beberapa ejaan lainnya. Tahun kelahirannya
				banyak pendapat. Ada yang mengatakan Al-Khawarizmi hidup sekitar
				pertengahan awal abad ke-9 M. Sumber lain mengatakan ia hidup di Khawarism,
				Usbekistan pada tahun 194 H/780 M, dan meninggal dunia tahun 266 H/850 M
				di Baghdad.
			</p>
			<p>Al-Khawarizmi, termasuk ilmuwan Muslim yang berpengetahuan luas, bukan hanya
				dalam bidang syariat, tetapi juga di bidang ﬁlsafat, logika, aritmatika, geometri,
				musik, ilmu hitung, sejarah Islam, dan kimia serta penulis ensiklopedia dalam
				berbagai disiplin. Dalam usia muda, dia bekerja di Bait al-Hikmah di bawah
				pemerintahan Khalifah Al-Makmun. Ia bekerja dalam sebuah observatorium (gedung
				untuk pengamatan dan penelitian) matematika dan astronomi. Al-Khawarizmi juga
				dipercaya untuk memimpin perpustakaan khalifah.
			</p>
			<p>Al-Khawarimi memperkenalkan angka-angka India dan cara-cara perhitungannya
				kepada dunia Islam. Ia adalah ilmuwan pertama kali yang memperkenalkan ilmu
				aljabar dan hisab. Pengetahuannya dalam bidang matematika menghasilkan
				konsep-konsep yang masih digunakan sampai sekarang. Karyanya yang paling
				terkenal adalah Al-Jabr wa al-Muqābalah, pemakaian secans dan tangens dalam
				penyelidikan trigonometri dan astronomi. Karya-karya Al-Khawarizmi di bidang
				aljabar telah diterjemahkan, antara lain oleh Gerard of Gremano dan Robert of
				Chaster ke dalam bahasa Eropa pada abad ke-12.
			</p>
		</div>
