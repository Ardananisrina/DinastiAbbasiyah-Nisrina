			<h1>Dinasti Bani Abbasiyah</h1>
	   	<p><b>A. Berdirinya Dinasti Bani Abbasiyah</b></p>
	   	<p>Keruntuhan Dinasti Umayyah pada tahun 750 M menjadi tonggak awal berdirinya
        Dinasti Abbasiyah. Khalifah pertamanya adalah Abdullah As-Saffah bin Muhammad
        bin Ali bin Abdulah bin Abbas bin Abdul Muṭalib. Dinamakan Dinasti Abbasiyah
        karena para pendirinya adalah keturunan Abbas ibn Abdul Muṭalib, paman Nabi
        Muhammad Saw. Masa kekuasaan Dinasti  Abbasiyah berlangsung dalam rentang waktu
        yang panjang, yaitu tahun 132 H/ 750 M s/d 656 H/1258 M. Dinasti Umayyah telah berhasil
        membawa kejayaan dunia Islam mulai dari Asia Barat, Asia Tengah, Asia Selatan,
        Afrika Utara hingga ke Eropa. Sementara itu, dunia Islam juga mengalami masa-masa
        kejayaan di bawah kekuasaan Dinasti Abbasiyah, terutama dalam bidang peradaban dan
        kebudayaan Islam sehingga kota Baghdad dikenal sebagai pusat peradaban dunia.
        Untuk lebih jelas, uraiannya sebagai berikut.
       </p>
       <p><b>1. Proses Pembentukan Dinasti Abbasiyah</b></p>
       <p>Upaya mengalahkan Dinasti Umayyah dilatarbelakangi
        pemikiran tentang siapa yang berhak memimpin setelah
        Rasulullah meninggal. Bani Hasyim (kaum Alawiyun)
        sebagai keturunan Rasulullah pernah mengemukakan hal
        tersebut. Terdapat tiga kota utama yang menjadi pusat
        kegiatan untuk menegakkan kekuasaan keluarga besar
        paman Rasulullah, Abbas bin Abdul Muṭalib, yaitu kota
        al-Humaymah sebagai pusat perencanaan, kota Kufah
        sebagai kota penghubung, dan kota Khurasan sebagai
        kota gerakan langsung (lapangan).
       </p>
       <p>Para keluarga Abbas melakukan berbagai strategi dan persiapan
        di ketiga tersebut. Salah satunya dengan mempropaganda bahwa
        orang-orang Abbasiyah lebih berhak dari pada Bani Umayyah atas
        kekhalifahan Islam. Mereka adalah keturunan Bani Hasyim yang
        nasabnya lebih dekat dengan Nabi Saw. Pemimpin gerakan ini
        adalah Imam Muhammad bin Ali, salah seorang keluarga Abbasiyah
        yang tinggal di Humaymah. Muhammad bin Ali tidak menonjolkan
        nama Bani Abbasiyah, melainkan menggunakan nama Bani Hasyim
        untuk menghindari perpecahan dengan kelompok Syi’ah. Strateginya
        berhasil menggabungkan berbagai kekuatan, terutama antara
        pendukung fanatik Ali bin Abi Ṭalib dengan kelompok lain.
      </p>
      <p>Gerakan dan propaganda yang dimotori oleh Muhammad bin Ali
        mendapat sambutan yang luar biasa dan tanggapan positif dari
        masyarakat, begitu juga dari golongan Mawali. Pada tahun 743
        M Muhammad bin Ali meninggal. Gerakannya dilanjutkan oleh
        putranya bernama Ibrahim al-Imam. Ia menunjuk Abu Muslim Al-
        Khurasani sebagai panglima perang karena sangat ahli dalam
        menarik simpati berbagai kelompok. Pernah dalam waktu satu hari,
        ia berhasil mengumpulkan penduduk dari sekitar 60 desa di Merv.
        Abu Muslim mengajak kelompok yang kecewa kepada Bani Umayah untuk
        mengembalikan kekhalifahan kepada Bani Hasyim, baik dari keturunan
        Abbas bin Abdul Muṭalib maupun dari keturunan Ali bin Abi Ṭalib.
      </p>
      <p>Akhirnya, Dinasti Umayyah mengalami kekalahan total dalam pertempuran.
        Khalifah Marwan II bersama 120.000 tentaranya, yang berusaha bertahan
        dengan menyebrangi sungai Tigris menuju Zab Hulu (Zab Besar), berhasil
        dikalahkan oleh gerakan kelompok Bani Hasyim. Khalifah Marwan II tewas
        dalam pertempuran di Busir (wilayah al- Fayyum) tahun 132 H/750 M.
        Kematian Khalifah Marwan II menjadi akhir dari runtuhnya Dinasti Umayyah,
        sekaligus menjadi awal berdirinya Dinasti Abbasiyah. Abul Abbas as-Ṣaffah
        merupakan khalifah pertamanya, sedangkan pusat kekuasaan awalnya
        ditempatkan di Kufah.
      </p>
